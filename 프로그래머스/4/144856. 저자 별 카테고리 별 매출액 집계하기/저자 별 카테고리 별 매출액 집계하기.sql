SELECT B.AUTHOR_ID, A.AUTHOR_NAME, B.CATEGORY, SUM(B.PRICE * S.SUM_SALES) AS TOTAL_SALES
FROM BOOK B
JOIN AUTHOR A ON B.AUTHOR_ID = A.AUTHOR_ID
JOIN (SELECT BOOK_ID, SUM(SALES) SUM_SALES
      FROM BOOK_SALES
      WHERE SALES_DATE >= '2022-01-01' AND SALES_DATE < '2022-02-01'
      GROUP BY BOOK_ID) S ON B.BOOK_ID = S.BOOK_ID
GROUP BY B.AUTHOR_ID, A.AUTHOR_NAME, B.CATEGORY
ORDER BY B.AUTHOR_ID, B.CATEGORY DESC